export const DEFAULT_THEME_ID = "white";

export function getThemeById(id: string): ThemeDefinition | undefined {
  return THEMES.find((t) => t.id === id);
}

export function applyTheme(themeId: string) {
  const theme = getThemeById(themeId);
  if (!theme) return;

  const root = document.documentElement;
  
  // Apply all CSS variables
  Object.entries(theme.vars).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });

  // Set data attribute for potential CSS selectors
  root.setAttribute("data-color-theme", themeId);

  // Also set the class for dark/light background behavior
  // All themes except "light" are dark-based
  const lightThemes = ["light", "white"];
  if (lightThemes.includes(themeId)) {
    root.classList.remove("dark");
  } else {
    root.classList.add("dark");
  }

  // Persist
  localStorage.setItem("cc-color-theme", themeId);
}

export function getStoredThemeId(): string {
  return localStorage.getItem("cc-color-theme") || DEFAULT_THEME_ID;
}
